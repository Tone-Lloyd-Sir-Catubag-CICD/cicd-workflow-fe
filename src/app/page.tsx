const YAML_INDENT: Record<YamlLineType, string> = {
  comment: "",
  key:     "",
  keyword: "    ",
  indent1: "  ",
  indent2: "    ",
  step:    "      - ",
};

function YamlLineValue({ value, valueType }: Readonly<{ value: string | null; valueType: "string" | "raw" | null }>) {
  if (value === null) {
    return <>:</>;
  }
  if (valueType === "string") {
    return <>{": "}<span className="mockup-string">{value}</span></>;
  }
  return <>{": "}{value}</>;
}

function YamlLineContent({ line }: Readonly<{ line: YamlLine }>) {
  const indent = YAML_INDENT[line.type];

  if (line.type === "comment") {
    return <span className="mockup-comment">{line.text}</span>;
  }
  if (line.type === "keyword") {
    return <>{indent}<span className="mockup-keyword">{line.text}</span>:</>;
  }
  return (
    <>
      {indent}
      <span className="mockup-key">{line.text}</span>
      <YamlLineValue value={line.value} valueType={line.valueType} />
    </>
  );
}

