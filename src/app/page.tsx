  if (status !== "signed-in") {
    return {
      primaryHref:    "/signup",
      primaryLabel:   "Sign up with Google or GitHub",
      appHref:        "/subscribe",
      appLabel:       "Pricing",
      navUtilityHref: "/login",
      navUtilityLabel: "Login",
    };
  }

  const primaryHref  = hasSubscription ? "/home"      : "/subscribe";
  const primaryLabel = hasSubscription ? "Open your workspace" : "Activate 300 pesos plan";

